package bean;

public class MarchingBean {
	private int marchingID;
	private String workuserNo;
	private int taskID;
	private String marchingTime;
	private int adminID;
	public int getMarchingID() {
		return marchingID;
	}
	public void setMarchingID(int marchingID) {
		this.marchingID = marchingID;
	}
	public String getWorkuserNo() {
		return workuserNo;
	}
	public void setWorkuserNo(String workuserNo) {
		this.workuserNo = workuserNo;
	}
	public int getTaskID() {
		return taskID;
	}
	public void setTaskID(int taskID) {
		this.taskID = taskID;
	}
	public String getMarchingTime() {
		return marchingTime;
	}
	public void setMarchingTime(String marchingTime) {
		this.marchingTime = marchingTime;
	}
	public int getAdminID() {
		return adminID;
	}
	public void setAdminID(int adminID) {
		this.adminID = adminID;
	}
	

}
